class VowelOrNot
{
	public static void main(String[] args)
	{
      
      char givenchar ='A';


      switch(givenchar)
      {
      	case 'a':
      	case 'e':
      	case 'i':
      	case 'o':
      	case 'u':
      	case 'A':
      	case 'E':
      	case 'I':
      	case 'O':
      	case 'U':
      	System.out.println("the given character "+givenchar +" is vowel");
      	break;
      	default:
      	System.out.println("the given character "+givenchar +" is not vowel");
      }
      }
      }
      





	
